// 마지막 영업일(주말이면 금요일)
function lastBusinessDay(): string {
  const d = new Date();
  const day = d.getDay();
  if (day === 0) d.setDate(d.getDate() - 2); // 일 → 금
  if (day === 6) d.setDate(d.getDate() - 1); // 토 → 금
  return d.toISOString().slice(0, 10);
}
